class Solution:
    def isValidSudoku(self, board) -> bool:
        """
        Determine if a 9 x 9 Sudoku board is valid. Only the filled cells need to be validated according to the
        following rules:

        Each row must contain the digits 1-9 without repetition.
        Each column must contain the digits 1-9 without repetition.
        Each of the nine 3 x 3 sub-boxes of the grid must contain the digits 1-9 without repetition.
        Note:

        A Sudoku board (partially filled) could be valid but is not necessarily solvable.
        Only the filled cells need to be validated according to the mentioned rules.

        Constraints:
        board.length == 9
        board[i].length == 9
        board[i][j] is a digit 1-9 or '.'.

        Plan:
        Use sets to determine validity.
        1. Verify that every row is valid.
        2. Verify that every column is valid.
        3. Verify that each 3x3 box is valid.
        At any point, if a violation is detected, return False. Otherwise, return true.
        """

        # Verify uniqueness in a row
        for i in range(9):
            row_set = set()
            for j in range(9):
                if board[i][j] == ".":
                    continue
                if board[i][j] in row_set:
                    return False
                else:
                    row_set.add(board[i][j])

        # Verify uniqueness in a column
        for i in range(9):
            column_set = set()
            for j in range(9):
                if board[i][j] == ".":
                    continue
                if board[j][i] in column_set:
                    return False
                else:
                    column_set.add(board[j][i])

        # Verify uniqueness in each 3x3 box

        for xmod in range(0, 9, 3):
            for ymod in range(0, 9, 3):
                current_box = set()
                for i in range(3):
                    for j in range(3):
                        if board[i+xmod][j+ymod] == ".":
                            continue
                        if board[i+xmod][j+ymod] in current_box:
                            return False
                        else:
                            current_box.add(board[i+xmod][j+ymod])




