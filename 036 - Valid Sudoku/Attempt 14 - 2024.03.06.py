class Solution:
    def isValidSudoku(self, board) -> bool:
        """
        Determine if a 9 x 9 Sudoku board is valid. Only the filled cells need to be validated according to the
        following rules:

        Each row must contain the digits 1-9 without repetition.
        Each column must contain the digits 1-9 without repetition.
        Each of the nine 3 x 3 sub-boxes of the grid must contain the digits 1-9 without repetition.
        Note:

        A Sudoku board (partially filled) could be valid but is not necessarily solvable.
        Only the filled cells need to be validated according to the mentioned rules.

        Constraints:
        board.length == 9
        board[i].length == 9
        board[i][j] is a digit 1-9 or '.'.
        """
        # Create row sets, col sets, and box sets
        row_arr = [set() for _ in range(9)]
        col_arr = [set() for _ in range(9)]
        box_arr = [set() for _ in range(9)]
        # Loop through board, and grab the current value.
        for row in range(9):
            for col in range(9):
                cur = board[row][col]
                # Handle the "." character by continuing
                if cur == ".":
                    continue
                # Check if cur is not in the proper row. If so, add it to that set. Otherwise return False.
                if cur not in row_arr[row]:
                    row_arr[row].add(cur)
                else:
                    return False
                # Check if cur is not in the proper col. If so, add it to that set. Otherwise return False.
                if cur not in col_arr[col]:
                    col_arr[col].add(cur)
                else:
                    return False
                # Check if cur is not in the proper box. If so, add it to that set. Otherwise return False.
                # Box calculation formula: box = ((row * 9) // 3) + (col // 3)
                box = ((row // 3) * 3) + (col // 3)
                if cur not in box_arr[box]:
                    box_arr[box].add(cur)
                else:
                    return False
        # If we complete the loop, then we've validated the entire board, so return True.
        return True

